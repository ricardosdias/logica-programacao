# ---------------------------------------------------
# Operadores de Comparação
# ---------------------------------------------------

# ---------------------------------------------------
# 1.0 Igualdade (==)
# ---------------------------------------------------
print("1.1 - Declare duas variáveis 'a' e 'b' e atribua valores inteiros a elas.")
print("Em seguida verifique se as duas variáveis são iguais:")
a = 10
b = 10
print(a == b)

# ---------------------------------------------------
# 2.0 Diferença (!=)
# ---------------------------------------------------
print("2.1 - Declare duas variáveis 'c' e 'd' e atribua valores inteiros a elas.")
print("Em seguida verifique se as duas variáveis são diferentes:")
c = 10
d = 20
print(c != d)

# ---------------------------------------------------
# 3.0 Maior que (>)
# ---------------------------------------------------
print("3.1 - Declare duas variáveis 'e' e 'f' e atribua valores inteiros a elas.")
print("Em seguida verifique se a variável 'e' é maior que a variável 'f':")
e = 10
f = 20
print(e > f)

# ---------------------------------------------------
# 4.0 Maior ou igual que (>=)
# ---------------------------------------------------
print("4.1 - Declare duas variáveis 'g' e 'h' e atribua valores inteiros a elas.")
print("Em seguida verifique se a variável 'g' é maior ou igual a variável 'h':")
g = 10
h = 10
print(g >= h)

# ---------------------------------------------------
# 5.0 Menor que (<)
# ---------------------------------------------------
print("5.1 - Declare duas variáveis 'i' e 'j' e atribua valores inteiros a elas.")
print("Em seguida verifique se a variável 'i' é menor que a variável 'j':")
i = 10
j = 20
print(i < j)

# ---------------------------------------------------
# 6.0 Menor ou igual que (<=)
# ---------------------------------------------------
print("6.1 - Declare duas variáveis 'k' e 'l' e atribua valores inteiros a elas.")
print("Em seguida verifique se a variável 'k' é menor ou igual a variável 'l':")
k = 10
l = 10
print(k <= l)

