# ---------------------------------------------------

# ---------------------------------------------------
# 1.0 Atribuição Simples (=)
# ---------------------------------------------------
# 1.1 - Atribua o valor 10 à variável a e imprima o valor de a
print("1.1 - Atribua o valor 10 à variável a e imprima o valor de a")

# 1.2 - Atribua o valor 20 à variável b e imprima o valor de b
print("1.2 - Atribua o valor 20 à variável b e imprima o valor de b")

# ---------------------------------------------------
# 2.0 Atribuição de Adição (+=)
# ---------------------------------------------------

# 2.1 - Atribua o valor 10 à variável c em seguida adicione 5 a ela, imprima o valor de c
print("2.1 - Atribua o valor 10 à variável c em seguida adicione 5 a ela, imprima o valor de c")

# 2.2 - Atribua o valor 20 à variável d em seguida adicione 10 a ela, imprima o valor de d
print("2.2 - Atribua o valor 20 à variável d em seguida adicione 10 a ela, imprima o valor de d")


# ---------------------------------------------------
# 3.0 Atribuição de Subtração (-=)
# ---------------------------------------------------

# 3.1 - Atribua o valor 100 à variável e em seguida subtraia 50 dela, imprima o valor de e
print("3.1 - Atribua o valor 100 à variável e em seguida subtraia 50 dela, imprima o valor de e")

# ---------------------------------------------------
# 4.0 Atribuição de Multiplicação (*=)
# ---------------------------------------------------

# 4.1 - Atribua o valor 10 à variável f em seguida multiplique por 5, imprima o valor de f
print("4.1 - Atribua o valor 10 à variável f em seguida multiplique por 5, imprima o valor de f")

# ---------------------------------------------------
# 5.0 Atribuição de Divisão (/=)
# ---------------------------------------------------

# 5.1 - Atribua o valor 100 à variável g em seguida divida por 5, imprima o valor de g
print("5.1 - Atribua o valor 100 à variável g em seguida divida por 5, imprima o valor de g")

# ---------------------------------------------------
# 6.0 Atribuição de Divisão Inteira (//=)
# ---------------------------------------------------

# 6.1 - Atribua o valor 100 à variável h em seguida divida por 3 (divisão inteira), imprima o valor de h
print("6.1 - Atribua o valor 100 à variável h em seguida divida por 3 (divisão inteira), imprima o valor de h")

# ---------------------------------------------------
# 7.0 Atribuição de Módulo (%=)
# ---------------------------------------------------

# 7.1 - Atribua o valor 100 à variável i em seguida divida por 3 (módulo), imprima o valor de i
print("7.1 - Atribua o valor 100 à variável i em seguida divida por 3 (módulo), imprima o valor de i")

# ---------------------------------------------------
# 8.0 Atribuição de Exponenciação (**=)
# ---------------------------------------------------

# 8.1 - Atribua o valor 3 à variável j em seguida eleve ao quadrado, imprima o valor de j
print("8.1 - Atribua o valor 3 à variável j em seguida eleve ao quadrado, imprima o valor de j")


