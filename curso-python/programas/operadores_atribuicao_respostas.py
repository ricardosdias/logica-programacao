# ---------------------------------------------------
# Operadores de Atribuição
# ---------------------------------------------------

# ---------------------------------------------------
# 1.0 Atribuição Simples (=)
# ---------------------------------------------------
print("1.1 - Atribua o valor 10 à variável a e imprima o valor de a")
a = 10
print(a)

print("1.2 - Atribua o valor 20 à variável b e imprima o valor de b")
b = 20
print(b)

# ---------------------------------------------------
# 2.0 Atribuição de Adição (+=)
# ---------------------------------------------------
print("2.1 - Atribua o valor 10 à variável c em seguida adicione 5 a ela, imprima o valor de c")
# Obs: Use o operador de atribuição de adição
c = 10
c += 5
print(c)

print("2.2 - Atribua o valor 20 à variável d em seguida adicione 10 a ela, imprima o valor de d")
# Obs: Use o operador de atribuição de adição
d = 20
d += 10
print(d)


# ---------------------------------------------------
# 3.0 Atribuição de Subtração (-=)
# ---------------------------------------------------
print("3.1 - Atribua o valor 100 à variável e em seguida subtraia 50 dela, imprima o valor de e")
# Obs: Use o operador de atribuição de subtração
e = 100
e -= 50
print(e)

# ---------------------------------------------------
# 4.0 Atribuição de Multiplicação (*=)
# ---------------------------------------------------
print("4.1 - Atribua o valor 10 à variável f em seguida multiplique por 5, imprima o valor de f")
# Obs: Use o operador de atribuição de multiplicação
f = 10
f *= 5
print(f)

# ---------------------------------------------------
# 5.0 Atribuição de Divisão (/=)
# ---------------------------------------------------
print("5.1 - Atribua o valor 100 à variável g em seguida divida por 5, imprima o valor de g")
# Obs: Use o operador de atribuição de divisão
g = 100
g /= 5
print(g)

# ---------------------------------------------------
# 6.0 Atribuição de Divisão Inteira (//=)
# ---------------------------------------------------
print("6.1 - Atribua o valor 100 à variável h em seguida divida por 3 (divisão inteira), imprima o valor de h")
# Obs: Use o operador de atribuição de divisão inteira
h = 100
h //= 3
print(h)

# ---------------------------------------------------
# 7.0 Atribuição de Módulo (%=)
# ---------------------------------------------------
print("7.1 - Atribua o valor 100 à variável i em seguida divida por 3 (módulo), imprima o valor de i")
# Obs: Use o operador de atribuição de módulo
i = 100
i %= 3
print(i)

# ---------------------------------------------------
# 8.0 Atribuição de Exponenciação (**=)
# ---------------------------------------------------
print("8.1 - Atribua o valor 3 à variável j em seguida eleve ao quadrado, imprima o valor de j")
# Obs: Use o operador de atribuição de exponenciação
j = 3
j **= 2
print(j)


# ---------------------------------------------------
# 9.0 - Exemplos Práticos
# ---------------------------------------------------

print("9.1 - Atualizar a idade de uma pessoa: atribua o valor 30 à variável idade e some 1 a ela, imprima o valor da idade")
# Obs: Use o operador de atribuição de adição
idade = 30
idade += 1
print(idade)

print("9.2 - Atualizar o saldo de uma conta bancária: atribua o valor 1000 à variável saldo e subtraia 100 dela, imprima o valor do saldo")
# Obs: Use o operador de atribuição de subtração
saldo = 1000
saldo -= 100
print(saldo)

print("9.3 - Atualizar a quantidade de produtos em estoque: atribua o valor 50 à variável estoque e multiplique por 2, imprima o valor do estoque")
# Obs: Use o operador de atribuição de multiplicação
estoque = 50
estoque *= 2
print(estoque)

print("9.4 - Atualizar o valor de um produto: atribua o valor 100 à variável produto aumente 10% a ela, imprima o valor do produto")
# Obs: Use o operador de atribuição de multiplicação
produto = 100
produto += produto * 0.10
print(produto)

print("9.5 - Dar um desconto de 20% em um produto: atribua o valor 100 à variável produto e subtraia 20% dela, imprima o valor do produto")
# Obs: Use o operador de atribuição de subtração
produto = 100
produto -= produto * 0.20
print(produto)
